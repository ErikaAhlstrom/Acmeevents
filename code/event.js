class Event {
  constructor(
    name = "unknown",
    start = "unknown",
    end = "unknown",
    time = "unknown",
    category = "unknown",
    location = "unknown",
    eventOwner = "unknown",
    admin = "unknown",
    participants = "unknown"
  ) {
    this.name = name;
    this.start = start;
    this.end = end;
    this.time = time;
    this.category = category;
    this.location = location;
    this.eventOwner = eventOwner;
    this.admin = admin;
    this.participants = participants;
  }
}
